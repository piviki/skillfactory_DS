# перебор элементов
# looping for
companies = ['Microsoft', 'Google', 'Oracle', 'Apple']
for item in companies:
    print(item)
# Microsoft
# Google
# Oracle
# Apple

# looping while
companies = ['Microsoft', 'Google', 'Oracle', 'Apple']
i = 0
while i < len(companies):
    print(companies[i])
    i+=1
# Microsoft
# Google
# Oracle
# Apple